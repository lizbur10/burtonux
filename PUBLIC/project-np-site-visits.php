				<h3>The Project</h3>
				<p>Site visits to a number of non-profit customer organizations to obtain a better understanding of this segment of Constant Contact's user base.</p>
			</div>

			<div>
				<h3>The Problem</h3>
				<p>This study arose in response to feedback from some non-profit customers about products and services that are geared more toward commercial customers and aren't as well suited to meet the needs of non-profits. Because non-profit organizations make up a sizable portion of Constant Contact's customer base, there was a need to gain a better understanding of the needs and usage patterns of this user segment.</p>
			</div>

			<div>
				<h3>The Approach</h3>
				<p>A major challenge was to obtain authentic, in-context information from the participating organizations. Because the cadence of most customers' work with the product is intermittent, purely observational techniques were not feasible. Therefore, a hybrid approach was used that included both an interview and a predetermined observational opportunity. The interviews provided consistent information across all participating organizations. For the observational portion, we asked participants to be sure to have a real email campaign that they could work on while we were there so we could observe their process.</p>
			</div>

			<div>
				<h3>The Result</h3>
				<p>Based on the results of the study we compiled:</p>
				<ul class="triangle-list">
					<li>Sub-categories of NP organizations, based on usage factors, priorities, limitations, etc.</li> 
					<li>Descriptions of usage factors that are particularly salient for this segment of users</li>
					<li>Ratings of the importance of each of the usage factors for each sub-category</li>
				</ul>
				<p>This information can help the company to better target the unique needs of this customer segment, and can also be used to inform persona development and refinement.</p>
			</div>

			<div>
				<h3>My Role</h3>
