				<h3>The Problem</h3>
				<p>The current design of the site de-emphasizes the freshest content &mdash; the patterns in the current issue &mdash; by devoting a disproportionate amount of real estate to static information that is rarely needed by visitors. This lack of balance, in combination with navigation that is confusing and inconsistent, makes it harder than necessary for users to find the content of most interest to them. In addition, the current site offers very limited opportunities for site users to interact with site content or other users.</p>
			</div>

			<div>
				<h3>The Approach</h3>
				<p>For the Information Architecture class, I conducted a content inventory and expert review of the site, developed a proposed site map using PowerPoint, and created paper &amp; pencil prototypes as well as annotated <a class="text-link" target="_blank" href="http://qfflzm.axshare.com/home.html">Axure prototypes</a> of the proposed redesign. For the Responsive Design class, I created a <a class="text-link" target="_blank" href="http://burtonux.com/knitty/">mobile-first coded prototype</a> of a redesigned home page.</p>
			</div>

			<div>
				<h3>The Result</h3>
				<p>The features of both versions of the redesigned home page include:</p>
				<ul class="triangle-list">
					<li>Increased emphasis on the freshest content</li>
					<li>Simplified navigation</li>
					<li>Increased opportunities for user interactions with site content (favorite, share, comment, etc.)</li>
				</ul>
