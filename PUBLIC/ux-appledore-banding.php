			<div>
				<h3>The Problem</h3>
				<p>The banding station's online presence was limited to a couple of faculty pages on a university website, designed for banding station volunteers. As a result, the information was difficult to locate and difficult for banding station staff to keep updated. Furthermore, the station had no public-facing online presence that might serve to attract potential new volunteers and supporters.</p>
			</div>

			<div>
				<h3>The Approach</h3>
				<p>I started by interviewing the head bander to gather information about her goals for the new site and then expanded the research to include all banding station volunteers. I then sketched out a proposed site map, created several style tiles showing some options for the visual treatment of the site, and worked with the head bander to <a class="text-link" target="_blank" href="http://burtonux.com/AIMS_styleTile/">finalize the style tile</a>. Finally, I <a class="text-link" href="dev-appledore-banding.php">developed the site</a> and launched it for the Spring 2016 bird banding season. I currently act as  the site's webmaster, and continue to gather feedback from users, add and revise content, and re-evaluate and refine the structure and organization of the site. </p>
			</div>

			<div>
				<h3>The Result</h3>
				<p>The features of the new website include:</p>
				<ul class="triangle-list">
					<li>engaging content to attract and retain site visitors, including images and daily updates of banding activity during banding seasons</li>
					<li>general information about the station and the research being conducted</li>
					<li>scheduling and logistical information for station volunteers</li>
					<li>the use of Wordpress as a CMS to make it easy to add and update content</li>
				</ul>
